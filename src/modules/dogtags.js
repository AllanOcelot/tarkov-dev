export function isAnyDogtag(id) {
    return (
        id === '59f32bb586f774757e1e8442' ||
        id === '59f32c3b86f77472a31742f0' ||
        id === 'customdogtags12345678910'
    );
}

export function isBothDogtags(id) {
    return id === 'customdogtags12345678910';
}

